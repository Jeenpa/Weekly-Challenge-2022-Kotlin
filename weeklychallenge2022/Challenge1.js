function anagrama(str1, str2){
    if (str1.toLowerCase() === str2.toLowerCase()){
        return false;
    } else{
        str1Arr = str1.toLowerCase().split('');
        str2Arr = str2.toLowerCase().split('');
        return str1Arr.sort().join() === str2Arr.sort().join()
    }
}

anagrama('amor', 'roma');